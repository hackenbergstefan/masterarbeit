inline void matmul(uint8_t *mat, uint8_t *vec, uint8_t *ret, 
        int m, uint8_t charac){
    int i,j;
    for(i=0;i<m;i++){
        ret[i] = (uint8_t) 0;
        for(j=0;j<m;j++){
            ret[i] += (uint8_t) mat[i*m + j]*vec[j];
        }
        ret[i] %= (uint8_t) charac;
    }
}

inline void initPoly8(uint8_t *p, int m) {
    int i;
    for(i=0;i<m;i++)
        p[i]=(uint8_t) 0;
}

inline void initPoly(int *p, int m) {
    int i;
    for(i=0;i<m;i++)
        p[i]=0;
}

